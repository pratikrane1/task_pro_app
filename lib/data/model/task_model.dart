class Autogenerated {
  bool? success;
  TaskModel? data;
  String? message;

  Autogenerated({this.success, this.data, this.message});

  Autogenerated.fromJson(Map<String, dynamic> json) {
    success = json['success'];
    data = json['data'] != null ? new TaskModel.fromJson(json['data']) : null;
    message = json['message'];
  }

  Map<String, dynamic> toJson() {
    final Map<String, dynamic> data = new Map<String, dynamic>();
    data['success'] = this.success;
    if (this.data != null) {
      data['data'] = this.data!.toJson();
    }
    data['message'] = this.message;
    return data;
  }
}

class TaskModel {
  int? completed;
  int? incomplete;
  String? taskProgess;
  List<TaskListModel>? taskList;

  TaskModel({this.completed, this.incomplete, this.taskProgess, this.taskList});

  TaskModel.fromJson(Map<String, dynamic> json) {
    completed = json['completed'];
    incomplete = json['incomplete'];
    taskProgess = json['task_progess'];
    if (json['task_list'] != null) {
      taskList = <TaskListModel>[];
      json['task_list'].forEach((v) {
        taskList!.add(new TaskListModel.fromJson(v));
      });
    }
  }

  Map<String, dynamic> toJson() {
    final Map<String, dynamic> data = new Map<String, dynamic>();
    data['completed'] = this.completed;
    data['incomplete'] = this.incomplete;
    data['task_progess'] = this.taskProgess;
    if (this.taskList != null) {
      data['task_list'] = this.taskList!.map((v) => v.toJson()).toList();
    }
    return data;
  }
}

